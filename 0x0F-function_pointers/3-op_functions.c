#include "3-calc.h"

/**
 * op_add - add two integers
 * @a: integer variable
 * @b: integer variable
 *
 * Return: integer, sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two integers
 * @a: integer variable
 * @b: integer variable
 *
 * Return: integer, difference between a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
